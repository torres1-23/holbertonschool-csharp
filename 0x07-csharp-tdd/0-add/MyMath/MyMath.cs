using System;

namespace MyMath
{
    ///<summary>Collection of mathematical operations.</summary>
    public class Operations
    {
        ///<summary>Adds two integers passed as arguments</summary>
        /// <param name="a">First integer to add.</param>
        /// <param name="b">Second integer to add.</param>
        /// <returns>The addition of two integers</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
